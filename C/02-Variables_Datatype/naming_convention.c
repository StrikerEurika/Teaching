# include <stdio.h>

int main() {

    // In C, the naming conventions are as follows:

    // 1. Names can contain letters, digits, and underscore.
    int myNum = 15;
    int _myNum = 15;
    int my2rdbroage_ = 15;
    
    
    // 2. Names must begin with a letter or underscores(_).
    // int 2myNum = 15;
    int _myNum = 15;
    int my2rdbroage_ = 15;


    // 3. Names are case-sensitive.
    int myNum = 15;
    int MYNUM = 10;


    // 4. Names cannot contain whitespaces or special characters(!, #, %, etc).
    // int my num = 19;
    int mynum = 18;

    // 5. Reserved words (like "int" in C) cannot be used as variable names.
    // float int = 2.5;

    // 6. In the case when we have long variable names

    // variable name: my crush age

    // Camel Case
    int myCrushAge = 19;

    // Snake Case
    int my_crush_age = 19;
    
    return 0;
}